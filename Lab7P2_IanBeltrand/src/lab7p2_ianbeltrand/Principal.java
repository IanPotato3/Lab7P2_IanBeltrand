    private void SignInButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SignInButtonMouseClicked
        // TODO add your handling code here:
        String username = UsernameS.getText();
        String password = PasswordS.getText();
        int age = Integer.parseInt(AgeS.getText());
        String Tipo = TipoComboBox.getSelectedItem().toString();
        
        if(admi.BuscarUsuario(username) == null){
            if(Tipo.equals("Cliente")){
                if(age >= 12){
                    Cliente cliente = new Cliente(username, password, age, Tipo);
                    admi.usuarios.add(cliente);
                    try {
                        admi.EscribirArchivo(cliente);
                    } catch (IOException ex) {
                        Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    
                    ActualC = cliente;
                    UsernameS.setText("");
                    PasswordS.setText("");
                    AgeS.setText("");
                    ActualizarListaCanciones();
                    Cliente.setModal(true);
                    Cliente.pack();
                    Cliente.setLocationRelativeTo(this);
                    Cliente.setVisible(true);

                    
                }else{
                    JOptionPane.showMessageDialog(SIGNING_IN, "No tienes la edad necesaria");
                }
            }else{
                if(Tipo.equals("Artista")){
                    if(age >= 18){
                        String nombreArtista = JOptionPane.showInputDialog(SIGNING_IN, "Ingresa tu nombre de artista: ");
                        Artista artista = new Artista(username, password, age, Tipo, nombreArtista);
                        admi.usuarios.add(artista);
                        try {
                            admi.EscribirArchivo(artista);
                        } catch (IOException ex) {
                            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        
                        ActualA = (Artista)artista;
                        UsernameS.setText("");
                        PasswordS.setText("");
                        AgeS.setText("");
                        ActualizarTreeArtista();
                        Artista.setModal(true);
                        Artista.pack();
                        Artista.setLocationRelativeTo(this);
                        Artista.setVisible(true);

                        
                    }else{
                        JOptionPane.showMessageDialog(SIGNING_IN, "No tienes la edad necesaria");
                    }
                }
            }
        }
    }//GEN-LAST:event_SignInButtonMouseClicked

    private void LoginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoginButtonMouseClicked
        // TODO add your handling code here:
        String username = UsernameL.getText();
        String password = PasswordL.getText();
        
        if(admi.BuscarUsuario(username) != null){
            Usuario usuario = admi.BuscarUsuario(username);
            if(usuario.getUsername().equals(username) && usuario.getPassword().equals(password)){
                if(usuario.Tipo.equals("Artista")){
                    ActualA = (Artista)usuario;
                    UsernameL.setText("");
                    PasswordL.setText("");
                    Artista.setModal(true);
                    Artista.pack();
                    Artista.setLocationRelativeTo(this);
                    Artista.setVisible(true);
                    
                    
                }else{
                    ActualC = (Cliente)usuario;
                    ActualizarListaCanciones();
                    UsernameL.setText("");
                    PasswordL.setText("");
                    Cliente.setModal(true);
                    Cliente.pack();
                    Cliente.setLocationRelativeTo(this);
                    Cliente.setVisible(true);
                    
                }
            }else{
                JOptionPane.showMessageDialog(this, "Password Incorrecta");
            }
        }else{
            JOptionPane.showMessageDialog(this, "No se encontraron dichas credenciales");
        }
    }//GEN-LAST:event_LoginButtonMouseClicked

